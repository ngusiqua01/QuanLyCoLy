    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        lbWarning.setVisible(false);
        int editIndex = tableDiaTang.getSelectedRow();
        if (editIndex == -1) {
            lbWarning.setText("bạn cần chọn công trình cần sửa trước ");
            lbWarning.setVisible(true);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tableDiaTang.getModel();
        String MaDT = model.getValueAt(editIndex, 1).toString();
        EditDiaTangDialog editDialog = new EditDiaTangDialog(this, false, MaDT, new EditRowListener() {
            @Override
            public void onSaveChange(String maDT) {
                initTable();
            }
        });

        editDialog.setVisible(true);
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int[] indicies = tableDiaTang.getSelectedRows();
        DefaultTableModel model = (DefaultTableModel) tableDiaTang.getModel();
        for (int i = 0; i < indicies.length; i++) {
            String MaDT = model.getValueAt(indicies[i], 1).toString();
            mDatabase.deleteDiaTang(MaDT);

        }
        Arrays.sort(indicies);
        for (int i = indicies.length - 1; i >= 0; i--) {
            model.removeRow(indicies[i]);
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

