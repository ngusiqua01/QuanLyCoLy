    public void initTable() {
        ArrayList<DiaTangItem> listDiaTangItems = mDatabase.getListDiaTang();
        System.out.println("quanlycoly.GUI.QuanLyThongTinDiaTang.initTable() listDiaTangItems = " + listDiaTangItems.size());
        DefaultTableModel model = (DefaultTableModel) tableDiaTang.getModel();
        int count = model.getRowCount();
        for (int i = 0; i < count; i++) {
            System.out.println("quanlycoly.GUI.QuanLyThongTinDiaTang.initTable() model.getRowCount() = " + model.getRowCount());
            model.removeRow(0);
        }
        for (int i = 0; i < listDiaTangItems.size(); i++) {
            DiaTangItem item = listDiaTangItems.get(i);
            model.addRow(new Object[]{i + 1, item.getMaDT(), item.getTenDT(), item.getMaCT(), item.getMota()});
        }

    }

